def load_audit_events():
    """
    Read and parse the Vault audit log file.

    Vault writes one JSON object per line (newline-delimited JSON / NDJSON).
    Each line is a complete audit record — either a 'request' or 'response' type.
    Returns a list of parsed dicts, oldest first.
    """
    if not LOG_PATH.exists():
        return []
    events = []
    for line in LOG_PATH.read_text(errors="replace").splitlines():
        line = line.strip()
        if line:
            try:
                events.append(json.loads(line))
            except json.JSONDecodeError:
                pass
    return events


def filter_responses(events):
    """Return only response-type events (these carry auth + policy data)."""
    return [e for e in events if e.get("type") == "response"]


def filter_auth_logins(events):
    """
    Return response events for JWT auth logins.
    These are the events that show WHO logged in, WHAT policy Vault applied,
    and WHICH entity was created/linked.
    """
    return [
        e for e in filter_responses(events)
        if "auth/jwt/login" in e.get("request", {}).get("path", "")
        and e.get("auth") is not None
    ]


def filter_cred_events(events):
    """
    Return response events for dynamic database credential issuance.
    These are the events that show WHICH user got a DB credential and WHAT role.
    """
    return [
        e for e in filter_responses(events)
        if "database/creds/" in e.get("request", {}).get("path", "")
    ]


def filter_token_events(events):
    """Return response events related to token lifecycle operations."""
    return [
        e for e in filter_responses(events)
        if e.get("request", {}).get("path", "").startswith("auth/token/")
    ]


def fmt_time(iso_str):
    """Format an ISO8601 timestamp to a readable local time string."""
    try:
        dt = datetime.fromisoformat(iso_str.replace("Z", "+00:00"))
        return dt.astimezone().strftime("%Y-%m-%d %H:%M:%S")
    except Exception:
        return iso_str


def vault_get(path, root_level=False):
    """
    Make a GET request to the Vault API.

    Args:
        path:       The Vault API path (e.g., "identity/entity/id/<id>")
        root_level: If True, omit the X-Vault-Namespace header.

    Returns the parsed JSON response body, or None on error.
    """
    headers = {"X-Vault-Token": VAULT_TOKEN}
    if not root_level and VAULT_NAMESPACE:
        headers["X-Vault-Namespace"] = VAULT_NAMESPACE
    try:
        r = requests.get(f"{VAULT_ADDR}/v1/{path}", headers=headers, timeout=5)
        r.raise_for_status()
        return r.json()
    except requests.exceptions.ConnectionError:
        st.error(f"Cannot reach Vault at {VAULT_ADDR}. Is vault-enterprise running?")
        return None
    except requests.exceptions.HTTPError as e:
        st.error(f"Vault API error: {e}")
        return None


def vault_list(path, root_level=False):
    """
    Make a LIST request to the Vault API.

    Vault's list operation uses the HTTP LIST method (not GET ?list=true).
    This is required for identity/entity, identity/group, etc.

    Args:
        path:       The Vault API path (e.g., "identity/entity")
        root_level: If True, omit the X-Vault-Namespace header.

    Returns the parsed JSON response body, or None on error.
    """
    headers = {"X-Vault-Token": VAULT_TOKEN}
    if not root_level and VAULT_NAMESPACE:
        headers["X-Vault-Namespace"] = VAULT_NAMESPACE
    try:
        r = requests.request(
            "LIST", f"{VAULT_ADDR}/v1/{path}", headers=headers, timeout=5
        )
        if r.status_code == 404:
            # Vault returns 404 when there are no entries — treat as empty list
            return {"data": {"keys": []}}
        r.raise_for_status()
        return r.json()
    except requests.exceptions.ConnectionError:
        st.error(f"Cannot reach Vault at {VAULT_ADDR}. Is vault-enterprise running?")
        return None
    except requests.exceptions.HTTPError as e:
        st.error(f"Vault API error: {e}")
        return None


def build_entity_map(auth_events):
    """
    Build a lookup dict: entity_id → display_name
    from the auth login events. Used to correlate credential issuance events
    (which carry entity_id but not the user's name) back to the human user.
    """
    entity_map = {}
    for e in auth_events:
        auth = e.get("auth", {})
        eid = auth.get("entity_id", "")
        name = auth.get("display_name", "")
        if eid and name:
            entity_map[eid] = name
    return entity_map


