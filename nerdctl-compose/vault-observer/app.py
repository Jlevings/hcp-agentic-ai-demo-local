    # Auth login responses → token created
    for e in auth_events:
        auth = e.get("auth", {})
        all_token_rows.append({
            "Time": fmt_time(e.get("time", "")),
            "Operation": "Token Created (JWT Login)",
            "User": auth.get("display_name", "—"),
            "TTL (s)": auth.get("token_ttl", "—"),
            "Policies": ", ".join(p for p in auth.get("policies", []) if p != "default"),
        })

    # Explicit token operations
    for e in token_events:
        auth = e.get("auth", {})
        req  = e.get("request", {})
        op   = req.get("path", "").replace("auth/token/", "").title()
        entity_id = auth.get("entity_id", "")
        user = entity_map.get(entity_id, auth.get("display_name", "—"))
        all_token_rows.append({
            "Time": fmt_time(e.get("time", "")),
            "Operation": op,
            "User": user,
            "TTL (s)": auth.get("token_ttl", "—"),
            "Policies": ", ".join(p for p in auth.get("policies", []) if p != "default"),
        })

    if not all_token_rows:
        st.info("No token events found yet. Sign in at http://localhost:8501 to generate tokens.")
    else:
        # Sort by time, newest first
        df = pd.DataFrame(all_token_rows).sort_values("Time", ascending=False)
        st.dataframe(df, use_container_width=True, hide_index=True)

    st.markdown("---")
    st.markdown(
        "**What this shows:** Every token is ephemeral. The MCP server's token expires "
        "after the TTL shown above — it cannot be reused. If the token leaks, it provides "
        "access only until expiry, and only to the paths permitted by the attached policy."
    )


