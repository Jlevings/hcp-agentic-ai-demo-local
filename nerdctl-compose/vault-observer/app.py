if page == "Auth Events":
    st.title("🔑 Auth Events")
    st.markdown(
        "Every time a user authenticates via Vault's JWT auth backend, "
        "Vault records the full event — including which policies were applied "
        "and which Vault entity was linked. This is the **first link** in the "
        "identity chain: User → Entra ID JWT → Vault entity → Policy."
    )

    events = load_audit_events()
    auth_events = filter_auth_logins(events)

    if not auth_events:
        st.info(
            "No auth events found yet. Make sure:\n"
            "1. Vault audit logging is enabled (`terraform apply` was run).\n"
            "2. A user has signed in at http://localhost:8501."
        )
        if not LOG_PATH.exists():
            st.warning(f"Audit log file not found at `{LOG_PATH}`. "
                       "Vault may not have written any events yet.")
    else:
        st.success(f"Found **{len(auth_events)}** auth login event(s).")

        rows = []
        for e in reversed(auth_events):  # newest first
            auth = e.get("auth", {})
            policies = auth.get("policies", [])
            # Remove default policy from display — it's always present
            visible_policies = [p for p in policies if p != "default"]
            rows.append({
                "Time": fmt_time(e.get("time", "")),
                "User": auth.get("display_name", "—"),
                "Policies Applied": ", ".join(visible_policies) if visible_policies else "default only",
                "Token TTL (s)": auth.get("token_ttl", "—"),
                "Entity ID": auth.get("entity_id", "—")[:8] + "…",  # truncate for display
            })

        df = pd.DataFrame(rows)
        st.dataframe(df, use_container_width=True, hide_index=True)

        # Raw event expander for deep inspection
        with st.expander("🔍 Inspect raw event JSON"):
            selected = st.selectbox(
                "Select event to inspect",
                options=range(len(auth_events)),
                format_func=lambda i: (
                    f"{fmt_time(auth_events[-(i+1)].get('time',''))} — "
                    f"{auth_events[-(i+1)].get('auth',{}).get('display_name','unknown')}"
                ),
            )
            st.json(auth_events[-(selected + 1)])

    st.markdown("---")
    st.markdown(
        "**What this shows:** Vault sees the user's Entra ID JWT, validates it against "
        "the JWKS endpoint, extracts the `groups` claim, maps group Object IDs to Vault "
        "identity group aliases, and applies the corresponding policy — all in one step."
    )


